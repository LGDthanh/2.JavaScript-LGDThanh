// Câu lệnh rẽ nhánh - If else
var date = 4;

if(date === 2){
    console.log('Thu 2');
} else if (date == 3){
    console.log('Thu 3');
} else {
    console.log('Không biết');
}
// Câu lệnh rẽ nhánh - Switch
switch(date){
    case 2: 
    console.log('Thu 2');
    break;
    case 3: 
    console.log('Thu 3');
    break;   
    case 4: 
    console.log('Thu 4');
    break; 
    case 5: 
    console.log('Thu 5');
    break;
    default:
        console.log('Không biết');
}
